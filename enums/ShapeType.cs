namespace Tetris.enums
{
    /// <summary>
    /// Изброим тип, който указва вида на формата (Shape), използвана за изграждане на тетромино.
    /// Позволява избор между различни визуални представяния.
    /// </summary>
    public enum ShapeType
    {
        /// <summary>Куб – основната форма в Tetris</summary>
        Cube,

        /// <summary>Сфера – използва се за демонстрация на различни форми</summary>
        Sphere,

        /// <summary>Цилиндър – резервиран за бъдеща поддръжка (в момента не се използва)</summary>
        Cylinder
    }
}