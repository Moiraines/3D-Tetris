namespace Tetris.enums
{
    /// <summary>
    /// Изброим тип, който представя състоянието на дадена форма (Shape) в игралното поле.
    /// Използва се за проверка при движение, рендиране и унищожаване на фигури.
    /// </summary>
    public enum ShapeState
    {
        /// <summary>Празно – формата е невидима и не участва в текущата игра</summary>
        Empty,

        /// <summary>Запълнено – формата е част от активна или кацнала фигура</summary>
        Solid
    }
}
